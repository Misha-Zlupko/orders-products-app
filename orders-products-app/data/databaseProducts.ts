export const db = {
  products: {
    toArray: async (): Promise<Product[]> => Promise.resolve([...productsDb]),
    add: async (product: Omit<Product, "id">): Promise<number> => {
      const id =
        productsDb.length === 0
          ? 1
          : Math.max(...productsDb.map((p) => p.id)) + 1;
      productsDb.push({ ...product, id });
      return id;
    },
  },
};
